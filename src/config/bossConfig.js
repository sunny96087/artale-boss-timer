// 獲取所有掉落物列表（用於自動完成提示）
export const getAllLoots = () => {
  const allLoots = new Set();
  bossConfig.forEach((boss) => {
    boss.commonLoots?.forEach((loot) => allLoots.add(loot));
    boss.rareLoots?.forEach((loot) => allLoots.add(loot));
  });
  return Array.from(allLoots).sort();
};

// 根據 BOSS ID 獲取掉落物
export const getLootsByBossId = (bossId) => {
  const boss = bossConfig.find((b) => b.id === bossId);
  if (!boss) return [];
  return [...(boss.commonLoots || []), ...(boss.rareLoots || [])];
};

// 根據 BOSS ID 獲取 BOSS 配置
export const getBossById = (bossId) => {
  return bossConfig.find((b) => b.id === bossId);
};
